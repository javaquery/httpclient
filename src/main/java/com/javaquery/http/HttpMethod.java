package com.javaquery.http;

/**
 * The enum Http method.
 *
 * @author javaquery
 * @since 1.0.0
 */
public enum HttpMethod {
    /**
     * Get http method.
     */
    GET,
    /**
     * Post http method.
     */
    POST,
    /**
     * Put http method.
     */
    PUT,
    /**
     * Delete http method.
     */
    DELETE;
}
